// ── 2. Default-patch render ─────────────────────────────────────────────────

/// One-note default-patch render. Holds C4 for 1 s, releases, lets the tail
/// decay over 4 s. Checks: nothing NaN-tails, the attack RMS sits inside the
/// listening-test envelope, the post-release tail dies below the noise floor.
///
/// Bounds depend on the patch tuning in ticket 0018 — if 0018 shifts the
/// patch's loudness materially, update these here.
#[test]
fn default_patch_render_one_note() {
    let entry = load_entry();
    let mut instance = instantiate(&entry);
    let mut processor = activate_started(&mut instance, MAX_FRAMES);
    let mut steady = 0u64;

    let sr = SAMPLE_RATE as usize;
    let mut l = Vec::with_capacity(sr * 5);
    let mut r = Vec::with_capacity(sr * 5);

    // Sample 0: NoteOn(60).
    let mut on_buf = EventBuffer::with_capacity(1);
    on_buf.push(&note_on(0, 60, 0.8));
    let mut out_events = EventBuffer::with_capacity(0);
    let mut first_l = vec![0.0_f32; MAX_FRAMES as usize];
    let mut first_r = vec![0.0_f32; MAX_FRAMES as usize];
    process_one(
        &mut processor,
        &on_buf,
        &mut out_events,
        None,
        &mut first_l,
        &mut first_r,
        steady,
    );
    steady += MAX_FRAMES as u64;
    l.extend_from_slice(&first_l);
    r.extend_from_slice(&first_r);

    // Render the rest of the held-note second.
    render_seconds(&mut processor, sr - MAX_FRAMES as usize, &mut steady, &mut l, &mut r);

    // Sample 0 of the 1-second mark: NoteOff(60).
    let mut off_buf = EventBuffer::with_capacity(1);
    off_buf.push(&note_off(0, 60));
    let mut release_l = vec![0.0_f32; MAX_FRAMES as usize];
    let mut release_r = vec![0.0_f32; MAX_FRAMES as usize];
    process_one(
        &mut processor,
        &off_buf,
        &mut out_events,
        None,
        &mut release_l,
        &mut release_r,
        steady,
    );
    steady += MAX_FRAMES as u64;
    l.extend_from_slice(&release_l);
    r.extend_from_slice(&release_r);

    // Render 4 s of release tail.
    render_seconds(&mut processor, sr * 4 - MAX_FRAMES as usize, &mut steady, &mut l, &mut r);

    assert_eq!(l.len(), sr * 5);
    assert_eq!(r.len(), sr * 5);

    for (i, (&a, &b)) in l.iter().zip(r.iter()).enumerate() {
        assert!(a.is_finite(), "non-finite L at sample {i}: {a}");
        assert!(b.is_finite(), "non-finite R at sample {i}: {b}");
    }

    let attack_start = (sr as f32 * 0.1) as usize;
    let attack_end = (sr as f32 * 0.9) as usize;
    let attack_l = &l[attack_start..attack_end];
    let attack_r = &r[attack_start..attack_end];
    let attack_rms = 0.5 * (rms_dbfs(attack_l) + rms_dbfs(attack_r));
    assert!(
        (-24.0..=-9.0).contains(&attack_rms),
        "attack RMS out of band: {attack_rms} dBFS (want [-24, -9])"
    );

    let tail_start = (sr as f32 * 4.5) as usize;
    let tail_end = (sr as f32 * 4.99) as usize;
    let tail_rms =
        0.5 * (rms_dbfs(&l[tail_start..tail_end]) + rms_dbfs(&r[tail_start..tail_end]));
    assert!(
        tail_rms <= -60.0,
        "tail RMS too loud: {tail_rms} dBFS (want ≤ -60)"
    );
}

// ── 3. Parameter sweep ──────────────────────────────────────────────────────

/// Every CLAP id at `min` / midpoint / `max`, 128 samples per setting, with
/// C4 held throughout. Cheap "extremes don't crash / NaN" guard.
///
/// Won't catch slow audio bugs (a filter that quietly NaN-tails over seconds
/// of post-sweep render escapes), but does catch denormal explosions, panics
/// in stepped-param boundaries, and the obvious crash classes. Mirrors VXN1.
#[test]
fn parameter_sweep_min_mid_max_stays_finite() {
    let entry = load_entry();
    let mut instance = instantiate(&entry);
    let mut processor = activate_started(&mut instance, MAX_FRAMES);
    let mut steady = 0u64;

    const CHUNK: usize = 128;

    let mut on_buf = EventBuffer::with_capacity(1);
    on_buf.push(&note_on(0, 64, 0.7));
    let mut out_events = EventBuffer::with_capacity(0);
    {
        let mut l = vec![0.0_f32; CHUNK];
        let mut r = vec![0.0_f32; CHUNK];
        process_one(
            &mut processor,
            &on_buf,
            &mut out_events,
            None,
            &mut l,
            &mut r,
            steady,
        );
        steady += CHUNK as u64;
    }

    for id in 0..TOTAL_PARAMS {
        let desc = desc_for_clap_id(id).expect("descriptor for id");
        let mid = ((desc.min as f64) + (desc.max as f64)) * 0.5;
        for &v in &[desc.min as f64, mid, desc.max as f64] {
            let mut buf = EventBuffer::with_capacity(1);
            buf.push(&param_event(0, id, v));
            let mut l = vec![0.0_f32; CHUNK];
            let mut r = vec![0.0_f32; CHUNK];
            process_one(
                &mut processor,
                &buf,
                &mut out_events,
                None,
                &mut l,
                &mut r,
                steady,
            );
            steady += CHUNK as u64;
            for (i, (&a, &b)) in l.iter().zip(r.iter()).enumerate() {
                assert!(
                    a.is_finite() && b.is_finite(),
                    "non-finite output for id={id} value={v} at sample {i}: \
                     L={a} R={b} (param {})",
                    desc.id
                );
            }
        }
    }
}

// ── 4. State save / restore round-trip ──────────────────────────────────────

/// Spread non-default values across the table via host param events, render
/// a few blocks (so `publish()` propagates the audio-mirror back to the
/// shared store), `save` into a `Vec<u8>`, instantiate a fresh plugin,
/// `load` the blob, and assert every CLAP id's `get_value` matches.
#[test]
fn state_round_trip_preserves_every_param() {
    let entry = load_entry();
    let mut instance = instantiate(&entry);
    let mut processor = activate_started(&mut instance, MAX_FRAMES);
    let mut steady = 0u64;

    // Mix of float / int / enum / bool ids spanning per-op / master /
    // matrix / FX. Each value sits inside the descriptor's range.
    let edits: &[(&str, f64)] = &[
        ("master-volume", -3.0),
        ("master-tune", 5.0),
        ("op1-num", 3.0),
        ("op6-level", 88.0),
        ("op4-pan", -0.7),
        ("mtx1-depth", 0.4),
        ("mtx8-depth", -0.7),
        ("reverb-decay", 4.5),
        ("delay-time", 250.0),
        ("assign-mode", 1.0),
    ];
    let mut buf = EventBuffer::with_capacity(edits.len());
    for &(name, v) in edits {
        let id = id_of(name).unwrap_or_else(|| panic!("unknown id {name}"));
        buf.push(&param_event(0, id, v));
    }
    let mut out_events = EventBuffer::with_capacity(0);
    let mut l = vec![0.0_f32; MAX_FRAMES as usize];
    let mut r = vec![0.0_f32; MAX_FRAMES as usize];
    process_one(
        &mut processor,
        &buf,
        &mut out_events,
        None,
        &mut l,
        &mut r,
        steady,
    );
    steady += MAX_FRAMES as u64;

    // A few more blocks so any audio-thread `publish()` settles fully.
    let empty = EventBuffer::with_capacity(0);
    for _ in 0..4 {
        process_one(
            &mut processor,
            &empty,
            &mut out_events,
            None,
            &mut l,
            &mut r,
            steady,
        );
        steady += MAX_FRAMES as u64;
    }

    // Snapshot post-publish values via the host params extension.
    let mut handle = instance.plugin_handle();
    let params_ext = handle
        .get_extension::<PluginParams>()
        .expect("params extension");
    let state_ext = handle
        .get_extension::<PluginState>()
        .expect("state extension");

    let expected: Vec<f64> = (0..TOTAL_PARAMS)
        .map(|i| {
            params_ext
                .get_value(&mut handle, ClapId::new(i as u32))
                .expect("get_value")
        })
        .collect();

    let mut blob: Vec<u8> = Vec::new();
    state_ext
        .save(&mut handle, &mut blob)
        .expect("state save failed");
    assert!(!blob.is_empty(), "save produced empty blob");

    // Fresh instance, identical entry. Load into it and compare.
    let mut fresh = instantiate(&entry);
    let mut fresh_handle = fresh.plugin_handle();
    let fresh_state = fresh_handle
        .get_extension::<PluginState>()
        .expect("state extension on fresh");
    fresh_state
        .load(&mut fresh_handle, &mut blob.as_slice())
        .expect("state load failed");
    let fresh_params = fresh_handle
        .get_extension::<PluginParams>()
        .expect("params extension on fresh");

    for i in 0..TOTAL_PARAMS {
        let got = fresh_params
            .get_value(&mut fresh_handle, ClapId::new(i as u32))
            .expect("get_value on fresh");
        let want = expected[i];
        let eps = match desc_for_clap_id(i).unwrap().kind {
            ParamKind::Float { .. } => 1e-4,
            _ => 1e-9,
        };
        assert!(
            (got - want).abs() <= eps,
            "param {i} round-trip mismatch: want {want} got {got}"
        );
    }
}

// ── 5. Transport tempo edits ────────────────────────────────────────────────

/// Render across a 90 → 180 BPM transport edit. The engine reads tempo on
/// every block, so all we're guarding here is "no panic / no NaN at the
/// boundary"; audible sync correctness is a manual listening test.
#[test]
fn tempo_edit_does_not_break_render() {
    let entry = load_entry();
    let mut instance = instantiate(&entry);
    let mut processor = activate_started(&mut instance, MAX_FRAMES);
    let mut steady = 0u64;

    let mut on_buf = EventBuffer::with_capacity(1);
    on_buf.push(&note_on(0, 60, 0.8));
    let mut out_events = EventBuffer::with_capacity(0);
    let mut l = vec![0.0_f32; MAX_FRAMES as usize];
    let mut r = vec![0.0_f32; MAX_FRAMES as usize];
    let empty = EventBuffer::with_capacity(0);

    let t90 = tempo_transport(90.0);
    process_one(
        &mut processor,
        &on_buf,
        &mut out_events,
        Some(&t90),
        &mut l,
        &mut r,
        steady,
    );
    steady += MAX_FRAMES as u64;
    for &v in l.iter().chain(r.iter()) {
        assert!(v.is_finite(), "non-finite at 90 BPM: {v}");
    }

    let t180 = tempo_transport(180.0);
    process_one(
        &mut processor,
        &empty,
        &mut out_events,
        Some(&t180),
        &mut l,
        &mut r,
        steady,
    );
    for &v in l.iter().chain(r.iter()) {
        assert!(v.is_finite(), "non-finite at 180 BPM: {v}");
    }
}

// ── 6. Pitch bend / mod wheel via raw MIDI ──────────────────────────────────

/// Send pitch bend at its 14-bit extremes (full ±2 st with default range)
/// plus CC1 at 0 → 127 while a note holds. Just guards against panics + NaN
/// across the bend / wheel input path.
#[test]
fn pitch_bend_and_mod_wheel_stay_finite() {
    let entry = load_entry();
    let mut instance = instantiate(&entry);
    let mut processor = activate_started(&mut instance, MAX_FRAMES);
    let mut steady = 0u64;

    let mut on_buf = EventBuffer::with_capacity(1);
    on_buf.push(&note_on(0, 60, 0.9));
    let mut out_events = EventBuffer::with_capacity(0);
    let mut l = vec![0.0_f32; MAX_FRAMES as usize];
    let mut r = vec![0.0_f32; MAX_FRAMES as usize];
    process_one(
        &mut processor,
        &on_buf,
        &mut out_events,
        None,
        &mut l,
        &mut r,
        steady,
    );
    steady += MAX_FRAMES as u64;

    let bend_max: u16 = 0x3FFF;
    let bend_min: u16 = 0x0000;
    let bend_split = |raw: u16| ((raw & 0x7F) as u8, ((raw >> 7) & 0x7F) as u8);
    let (max_lsb, max_msb) = bend_split(bend_max);
    let (min_lsb, min_msb) = bend_split(bend_min);

    let mut midi_buf = EventBuffer::with_capacity(4);
    midi_buf.push(&MidiEvent::new(0, 0, [0xE0, max_lsb, max_msb]));
    midi_buf.push(&MidiEvent::new(64, 0, [0xE0, min_lsb, min_msb]));
    midi_buf.push(&MidiEvent::new(128, 0, [0xB0, 1, 0]));
    midi_buf.push(&MidiEvent::new(192, 0, [0xB0, 1, 127]));

    process_one(
        &mut processor,
        &midi_buf,
        &mut out_events,
        None,
        &mut l,
        &mut r,
        steady,
    );
    for (i, (&a, &b)) in l.iter().zip(r.iter()).enumerate() {
        assert!(
            a.is_finite() && b.is_finite(),
            "non-finite under bend / mod-wheel at sample {i}: L={a} R={b}"
        );
    }
}

// ── 6. Filter latency reporting (ticket 0086) ───────────────────────────────

/// The CLAP latency extension reports 0 with the filter off (the render path
/// is the unchanged sample-major bypass) and the halfband interp+decimate
/// round-trip group delay — derived in `vxn2_dsp::halfband` — at each
/// oversample factor when on. Filter params are pushed through the host's
/// main-thread (inactive) param flush, exactly the structural-edit path PDC
/// re-reads `get()` after.
#[test]
fn latency_extension_reports_filter_group_delay() {
    let entry = load_entry();
    let mut instance = instantiate(&entry);

    let enable = id_of("filter-enable").expect("filter-enable");
    let os = id_of("filter-oversample").expect("filter-oversample");

    // Push one param edit via the inactive main-thread flush.
    let set = |instance: &mut PluginInstance<TestHost>, id: usize, value: f64| {
        let params = instance
            .plugin_handle()
            .get_extension::<PluginParams>()
            .expect("params extension");
        let mut buf = EventBuffer::with_capacity(1);
        buf.push(&param_event(0, id, value));
        let mut out = EventBuffer::with_capacity(0);
        let mut inactive = instance
            .inactive_plugin_handle()
            .expect("plugin is inactive");
        params.flush(&mut inactive, &buf.as_input(), &mut out.as_output());
    };

    let latency = |instance: &mut PluginInstance<TestHost>| -> u32 {
        let mut handle = instance.plugin_handle();
        let ext = handle
            .get_extension::<PluginLatency>()
            .expect("latency extension");
        ext.get(&mut handle)
    };

    // Filter off by default → no added latency.
    assert_eq!(latency(&mut instance), 0, "filter off");

    // Enabled: enum index → factor 1/2/4/8 → round-trip base-rate latency.
    set(&mut instance, enable, 1.0);
    for (idx, want) in [(0u32, 0u32), (1, 16), (2, 24), (3, 28)] {
        set(&mut instance, os, idx as f64);
        assert_eq!(
            latency(&mut instance),
            want,
            "filter on, oversample idx {idx}"
        );
    }

    // Disabling returns to 0 even with the oversample selector left at 8×.
    set(&mut instance, enable, 0.0);
    assert_eq!(latency(&mut instance), 0, "filter off again");
}
