static VXN_ENTRY: EntryDescriptor = clack_entry!(SinglePluginEntry<VxnPlugin>);

const SAMPLE_RATE: f64 = 44_100.0;
const MAX_FRAMES: u32 = 256;
const PLUGIN_ID: &core::ffi::CStr = c"labs.vulpus.vxn2";

// ── Host stub ───────────────────────────────────────────────────────────────

struct TestHostShared;
impl SharedHandler<'_> for TestHostShared {
    fn request_restart(&self) {}
    fn request_process(&self) {}
    fn request_callback(&self) {}
}

struct TestHost;
impl HostHandlers for TestHost {
    type Shared<'a> = TestHostShared;
    type MainThread<'a> = ();
    type AudioProcessor<'a> = ();
}

fn load_entry() -> PluginEntry {
    // SAFETY: VXN_ENTRY is generated by clack's own macro.
    unsafe { PluginEntry::load_from_raw(&VXN_ENTRY, c"/tmp/VXN2.clap") }.unwrap()
}

fn host_info() -> HostInfo {
    HostInfo::new("VXN2 Smoke", "Vulpus Labs", "https://vulpus.labs", "0.0.0").unwrap()
}

fn instantiate(entry: &PluginEntry) -> PluginInstance<TestHost> {
    PluginInstance::<TestHost>::new(
        |_| TestHostShared,
        |_| (),
        entry,
        PLUGIN_ID,
        &host_info(),
    )
    .expect("instantiation failed")
}

fn activate_started(
    instance: &mut PluginInstance<TestHost>,
    max_frames: u32,
) -> StartedPluginAudioProcessor<TestHost> {
    let cfg = PluginAudioConfiguration {
        sample_rate: SAMPLE_RATE,
        min_frames_count: 1,
        max_frames_count: max_frames,
    };
    instance
        .activate(|_, _| (), cfg)
        .expect("activation failed")
        .start_processing()
        .expect("start_processing failed")
}

/// Drive one `process()` call. Output port is stereo f32; the synth has no
/// audio inputs so we hand it an empty `InputAudioBuffers`.
fn process_one(
    processor: &mut StartedPluginAudioProcessor<TestHost>,
    in_events: &EventBuffer,
    out_events: &mut EventBuffer,
    transport: Option<&TransportEvent>,
    out_l: &mut [f32],
    out_r: &mut [f32],
    steady_time: u64,
) {
    assert_eq!(out_l.len(), out_r.len(), "channel length mismatch");
    let inputs = InputAudioBuffers::empty();
    let mut output_ports = AudioPorts::with_capacity(2, 1);
    let chans: [&mut [f32]; 2] = [out_l, out_r];
    let mut outputs = output_ports.with_output_buffers([AudioPortBuffer {
        channels: AudioPortBufferType::f32_output_only(chans.into_iter()),
        latency: 0,
    }]);
    let _ = processor
        .process(
            &inputs,
            &mut outputs,
            &in_events.as_input(),
            &mut out_events.as_output(),
            Some(steady_time),
            transport,
        )
        .expect("process failed");
}

/// Render `frames` samples worth of audio in `MAX_FRAMES`-size chunks,
/// appending the stereo result to `l_out` / `r_out`.
fn render_seconds(
    processor: &mut StartedPluginAudioProcessor<TestHost>,
    frames: usize,
    steady_time: &mut u64,
    l_out: &mut Vec<f32>,
    r_out: &mut Vec<f32>,
) {
    let in_events = EventBuffer::with_capacity(0);
    let mut out_events = EventBuffer::with_capacity(0);
    let mut remaining = frames;
    while remaining > 0 {
        let n = remaining.min(MAX_FRAMES as usize);
        let mut l = vec![0.0_f32; n];
        let mut r = vec![0.0_f32; n];
        process_one(
            processor,
            &in_events,
            &mut out_events,
            None,
            &mut l,
            &mut r,
            *steady_time,
        );
        *steady_time += n as u64;
        l_out.extend_from_slice(&l);
        r_out.extend_from_slice(&r);
        remaining -= n;
    }
}

fn rms_dbfs(samples: &[f32]) -> f32 {
    if samples.is_empty() {
        return f32::NEG_INFINITY;
    }
    let sum_sq: f64 = samples.iter().map(|&x| (x as f64) * (x as f64)).sum();
    let rms = (sum_sq / samples.len() as f64).sqrt() as f32;
    if rms <= 0.0 {
        f32::NEG_INFINITY
    } else {
        20.0 * rms.log10()
    }
}

fn note_on(time: u32, key: u16, velocity: f64) -> NoteOnEvent {
    NoteOnEvent::new(time, Pckn::new(0_u16, 0_u16, key, 0_u32), velocity)
}

fn note_off(time: u32, key: u16) -> NoteOffEvent {
    NoteOffEvent::new(time, Pckn::new(0_u16, 0_u16, key, 0_u32), 0.0)
}

fn param_event(time: u32, id: usize, value: f64) -> ParamValueEvent {
    ParamValueEvent::new(
        time,
        ClapId::new(id as u32),
        Pckn::match_all(),
        value,
        Cookie::empty(),
    )
}

/// Hand-rolled tempo transport event. clack-common doesn't expose a builder
/// for `TransportEvent`, so we fill the struct directly with `HAS_TEMPO` set.
fn tempo_transport(bpm: f64) -> TransportEvent {
    TransportEvent {
        header: EventHeader::new_core(0, EventFlags::empty()),
        flags: TransportFlags::HAS_TEMPO,
        song_pos_beats: BeatTime::from_int(0),
        song_pos_seconds: SecondsTime::from_int(0),
        tempo: bpm,
        tempo_inc: 0.0,
        loop_start_beats: BeatTime::from_int(0),
        loop_end_beats: BeatTime::from_int(0),
        loop_start_seconds: SecondsTime::from_int(0),
        loop_end_seconds: SecondsTime::from_int(0),
        bar_start: BeatTime::from_int(0),
        bar_number: 0,
        time_signature_numerator: 4,
        time_signature_denominator: 4,
    }
}

